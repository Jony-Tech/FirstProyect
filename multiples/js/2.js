function numero() {
    let numero = document.getElementById('n').value;

    switch (numero) {
        case '1':
            swal('One', '1')
            break;
        case '2':
            swal('Two', '2')
            break;
        case '3':
            swal('Three', '3')
            break;

        case '4':
            swal('Four', '4')
            break;

        case '5':
            swal('Five', '5')
            break;

        case '6':
            swal('Six', '6')
            break;

        case '7':
            swal('Seven', '7')
            break;

        case '8':
            swal('Eight', '8')
            break;

        case '9':
            swal('Nine', '9')
            break;

        case '10':
            swal('Ten', '10')
            break;

        default:
            break;
    }
}